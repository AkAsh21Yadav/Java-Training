import java.util.ArrayList;

public class SampleArrayListExampleofGenericType 
{
	public static void main(String[] args) 
	{
		ArrayList<Integer> marks = new ArrayList<>(); //Homogenous
		marks.add(15);
		marks.add(16);
		marks.add(17);
		marks.add(18);
		marks.add(19);
		
		System.out.println("Marks : "+marks);
		System.out.println("Size :"+marks.size());
		System.out.println("isEmpty : "+marks.isEmpty());
		System.out.println("Removing Element : "+marks.remove(2));
		System.out.println("After removing : "+marks);
		System.out.println("Contain elements : "+marks.contains(19));
		marks.clear();
		System.out.println("Marks after clearing : "+marks);
		System.out.println("Size after clearing :"+marks.size());		
	}
}
